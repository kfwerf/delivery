export type IconProps = {
  name?: IconName;
};

export function iconNameToString(name: IconName): string {
  return name?.toLowerCase()?.replace(/_/gi, '-');
}

export default function Icon(props: IconProps): JSX.Element {
  const { name } = props;
  const iconClazz = `icon-${iconNameToString(name)}`;
  const classes = ['icon', iconClazz].join(' ');
  return <span className={classes}>{iconClazz}</span>;
}
